List<Reciter> searchReciter(String receiterName) {
  if (receiterName.trim().isEmpty) {
    return _reciters;
  }

  return _reciters.where((reciter) {
    return reciter.englishName
        .toLowerCase()
        .contains(receiterName.toLowerCase());
  }).toList();
}
