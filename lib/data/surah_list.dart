List<Surah> searchSurah(String surahName) {
  if (surahName.trim().isEmpty) {
    return _surahList;
  }

  return _surahList.where((surah) {
    return surah.englishName.toLowerCase().contains(surahName.toLowerCase());
  }).toList();
}
